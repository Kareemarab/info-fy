extension UIColor {
    
    static func shopifyGreen() -> UIColor {
        return UIColor(red:0.59, green:0.75, blue:0.28, alpha:1.0)
    }
    
    static func shopifyBlue() -> UIColor {
        return UIColor(red:0.28, green:0.61, blue:0.81, alpha:1.0)
    }
    
    static func shopifyPurple() -> UIColor {
        return UIColor(red:0.36, green:0.42, blue:0.77, alpha:1.0)
    }
    
    static func shopifyBlack() -> UIColor {
        return UIColor(red:0.18, green:0.21, blue:0.22, alpha:1.0)
    }
    
    static func shopifyOffwhite() -> UIColor {
        return UIColor(red:0.96, green:0.96, blue:0.96, alpha:1.0)
    }
    
    static func shopifyLightBlue() -> UIColor {
        return UIColor(red:0.95, green:0.97, blue:0.98, alpha:1.0)
    }
    
    static func shopifyGray() -> UIColor {
        return UIColor(red:0.40, green:0.40, blue:0.40, alpha:1.0)
    }
    
}
